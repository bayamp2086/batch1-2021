package com.bayamp.training.personal.arrays;

public class NestedForLoopExample {

	public static void main(String[] args) {
		
		
		for (int i = 0; i < 3; i++) { //out for loop begin
			System.out.println("*************");
			
			System.out.println("\n");
			
			for (int j = 0; j < 5; j++) { //inner for loop begin
				System.out.println("*************");	
			} //inner for loop end
			
			System.out.println("\n");
			
		}//out for loop end

		

	}

}
